const allTranslations = {};
for (const lang of config.locales.available) {
    allTranslations[lang] = {
        ...(dashboardTranslations[lang] || {}),
        ...(botTranslations[lang] || {})
    };
}

const defaultLang = config.locales.default || 'en';
const rtlLangs = config.locales.rtl || [];

function t(key, lang) {
    const l = allTranslations[lang || defaultLang];
    if (l && l[key]) return l[key];
    const fallback = allTranslations[defaultLang];
    if (fallback && fallback[key]) return fallback[key];
    return key;
}

function tWithVars(key, vars, lang) {
    let str = t(key, lang);
    if (!vars) return str;
    for (const [k, v] of Object.entries(vars)) {
        str = str.replace(`{${k}}`, v);
    }
    return str;
}

function isRTL(lang) {
    return rtlLangs.includes(lang || defaultLang);
}

function direction(lang) {
    return isRTL(lang) ? 'rtl' : 'ltr';
}

function fromDiscordLocale(discordLocale, interaction) {
    if (interaction && interaction._resolvedLocale) return interaction._resolvedLocale;
    return LOCALE_MAP[discordLocale] || defaultLang;
}

// Resolve locale with optional guild-level override
function guildLocale(guildLocale, discordLocale) {
    if (guildLocale && config.locales.available.includes(guildLocale)) return guildLocale;
    return fromDiscordLocale(discordLocale);
}

module.exports = { t, tWithVars, isRTL, direction, fromDiscordLocale, guildLocale, defaultLang, allTranslations };